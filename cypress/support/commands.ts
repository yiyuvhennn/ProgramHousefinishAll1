Cypress.Commands.add('resetDb', ()=> {
  const apiUrl = Cypress.env('apiUrl')
  return cy
    .request({
      method: 'POST',
      url: `${apiUrl}/test/reset`,
      // ✅ 這邊建議先保持「不吞錯」：如果 403/404 直接讓測試失敗
      failOnStatusCode: true,
    })
    .its('status')
    .should('eq', 200)
})
